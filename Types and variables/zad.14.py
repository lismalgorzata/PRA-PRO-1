  
#####
# Calculation of the area and circumference of a circle
##
# determine radius and PI
r=5
pi=3.14
# calculate area
area=r*pi
# calculate circumference
circumference=2*r*pi
# display results
f_area="{:.2f}".format(area)
f_circumference="{:.2f}".format(circumference)
print(f"The area of a circle is {f_area} and its circumference is {f_circumference}")